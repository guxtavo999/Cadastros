def somar(a, b):
    soma = a + b
    return soma


def subtrair(c, d):
    if c > d:
        sub = c - d
    else:
        sub = d - c
    return sub


def multiplicar(e, f):
    mult = e * f
    return mult


def dividir(g, j):
    if g > j:
        div = g / j
    else:
        div = j / g
    return div
